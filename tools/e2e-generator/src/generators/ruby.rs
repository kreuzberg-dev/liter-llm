use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct RubyGenerator;

impl Generator for RubyGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let ruby_dir = output_root.join("ruby");

        let _ = std::fs::remove_dir_all(&ruby_dir);
        std::fs::create_dir_all(ruby_dir.join("spec"))
            .map_err(|e| anyhow::anyhow!("Failed to create ruby/spec dir: {e}"))?;

        write_gemfile(&ruby_dir)?;
        write_rspec_config(&ruby_dir)?;
        write_rubocop_yml(&ruby_dir)?;
        write_spec_helper(&ruby_dir)?;
        write_helpers(&ruby_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_spec_file(&ruby_dir, category, cat_fixtures)?;
        }

        // Run rubocop autocorrect best-effort.
        let _ = std::process::Command::new("bundle")
            .args(["exec", "rubocop", "--autocorrect-all", "--no-color", "-q"])
            .current_dir(ruby_dir.as_str())
            .status();

        println!("Generated Ruby e2e tests in {ruby_dir}");
        Ok(())
    }
}

