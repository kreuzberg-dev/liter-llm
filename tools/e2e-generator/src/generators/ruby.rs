fn emit_ruby_stream_test(out: &mut String, fixture: &Fixture, endpoint: &str) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    let is_error = fixture.api.mock_response.status >= 400 || !fixture.assertions.expect_success;

    writeln!(
        out,
        "    response = post_json(server.url, {:?}, {})",
        endpoint,
        ruby_string(&req_json)
    )
    .unwrap();
    writeln!(out).unwrap();

    if is_error {
        writeln!(
            out,
            "    expect(response.code.to_i).to eq({})",
            fixture.api.mock_response.status
        )
        .unwrap();
        writeln!(out, "    expect(response.code.to_i).to be >= 400").unwrap();
        return;
    }

    writeln!(out, "    expect(response.code.to_i).to eq(200)").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    chunks = parse_sse_chunks(response.body)").unwrap();

    let meaningful: usize = fixture
        .api
        .mock_response
        .stream_chunks
        .iter()
        .filter(|c| {
            c.get("choices")
                .and_then(|ch| ch.as_array())
                .and_then(|arr| arr.first())
                .and_then(|ch| ch.get("delta"))
                .and_then(|d| d.get("content"))
                .and_then(|v| v.as_str())
                .is_some_and(|s| !s.is_empty())
        })
        .count();

    // For empty stream fixtures, min is 0; otherwise require at least 1 chunk.
    let min_chunks = if fixture.api.mock_response.stream_chunks.is_empty() {
        0
    } else {
        meaningful.max(1)
    };
    writeln!(out, "    expect(chunks.size).to be >= {min_chunks}").unwrap();

    let expected_content: String = fixture
        .api
        .mock_response
        .stream_chunks
        .iter()
        .filter_map(|c| {
            c.get("choices")
                .and_then(|ch| ch.as_array())
                .and_then(|arr| arr.first())
                .and_then(|ch| ch.get("delta"))
                .and_then(|d| d.get("content"))
                .and_then(|v| v.as_str())
                .map(str::to_owned)
        })
        .collect();

    if !expected_content.is_empty() {
        writeln!(out).unwrap();
        writeln!(out, "    content = chunks.filter_map do |raw|").unwrap();
        writeln!(out, "      parsed = JSON.parse(raw) rescue nil").unwrap();
        writeln!(out, "      next unless parsed").unwrap();
        writeln!(out, "      parsed.dig(\"choices\", 0, \"delta\", \"content\")").unwrap();
        writeln!(out, "    end.join").unwrap();
        writeln!(out, "    expect(content).to eq({})", ruby_string(&expected_content)).unwrap();
    }
}

