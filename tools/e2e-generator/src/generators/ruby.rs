fn write_helpers(dir: &Utf8Path) -> Result<()> {
    // Use r##"..."## so that the Ruby "#{...}" interpolation sequences inside
    // the string do not accidentally terminate the Rust raw-string literal.
    let content = r##"# Code generated by liter-llm-e2e-generator. DO NOT EDIT.
# frozen_string_literal: true

require "json"
require "net/http"
require "uri"
require "webrick"

module E2EHelpers
  # MockRoute holds the configuration for a single mock HTTP route.
  MockRoute = Struct.new(:path, :method, :status, :body, :stream_chunks, keyword_init: true)

  # MockServer starts a WEBrick HTTP server on a random port.
  # It matches requests by path + method and returns the configured response.
  class MockServer
    attr_reader :url

    def initialize(routes)
      @routes = routes
      @server = WEBrick::HTTPServer.new(
        Port: 0,
        Logger: WEBrick::Log.new(File::NULL),
        AccessLog: []
      )
      @port = @server.config[:Port]
      @url = "http://127.0.0.1:#{@port}"

      @routes.each do |route|
        @server.mount_proc(route.path) do |req, res|
          if req.request_method.upcase == route.method.upcase
            if route.stream_chunks && !route.stream_chunks.empty?
              res.status = route.status
              res["Content-Type"] = "text/event-stream"
              res["Cache-Control"] = "no-cache"
              body_parts = route.stream_chunks.map { |chunk| "data: #{chunk}\n\n" }
              body_parts << "data: [DONE]\n\n"
              res.body = body_parts.join
            else
              res.status = route.status
              res["Content-Type"] = "application/json"
              res.body = route.body.to_s
            end
          else
            res.status = 405
            res.body = "Method Not Allowed"
          end
        end
      end

      @thread = Thread.new { @server.start }
      # Give the server a moment to start accepting connections.
      sleep 0.05
    end

    def stop
      @server.shutdown
      @thread.join
    end
  end

  # post_json sends a POST request with a JSON body and returns the Net::HTTP::Response.
  def post_json(url, path, body)
    uri = URI.parse("#{url}#{path}")
    http = Net::HTTP.new(uri.host, uri.port)
    request = Net::HTTP::Post.new(uri.path, "Content-Type" => "application/json")
    request.body = body.is_a?(String) ? body : body.to_json
    http.request(request)
  end

  # get_json sends a GET request and returns the Net::HTTP::Response.
  def get_json(url, path)
    uri = URI.parse("#{url}#{path}")
    Net::HTTP.get_response(uri)
  end

  # parse_sse_chunks reads SSE lines from a string body.
  # Returns data payloads, excluding the "[DONE]" sentinel.
  def parse_sse_chunks(body)
    body.lines.filter_map do |line|
      line = line.strip
      next unless line.start_with?("data: ")

      payload = line.sub(/^data: /, "")
      payload unless payload == "[DONE]"
    end
  end
end

RSpec.configure do |config|
  config.include E2EHelpers
end
"##;
    std::fs::write(dir.join("spec").join("helpers.rb"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write spec/helpers.rb: {e}"))
}

// ─── Spec file generation ─────────────────────────────────────────────────────

fn write_spec_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    // Skip parity tests — they verify API surface, not behavior.
    if category == "parity" {
        return Ok(());
    }

    // Filter out fixtures that are skipped for Ruby.
    let active_fixtures: Vec<&Fixture> = fixtures
        .iter()
        .filter(|f| !f.skip.languages.iter().any(|l| l == "ruby"))
        .copied()
        .collect();

    if active_fixtures.is_empty() {
        return Ok(());
    }

    // Dispatch to category-specific writers for new categories.
    match category {
        "cache" => return write_cache_specs(dir, &active_fixtures),
        "budget" => return write_budget_specs(dir, &active_fixtures),
        "hooks" => return write_hooks_specs(dir, &active_fixtures),
        "custom_provider" => return write_custom_provider_specs(dir, &active_fixtures),
        _ => {}
    }

    let mut out = String::new();

    writeln!(out, "# Code generated by liter-llm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out, "# frozen_string_literal: true").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "require \"spec_helper\"").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "RSpec.describe {:?} do", category).unwrap();

    for fixture in &active_fixtures {
        write_example(&mut out, fixture);
    }

    writeln!(out, "end").unwrap();

    let out = out.trim_end().to_string() + "\n";
    let filename = format!("{}_spec.rb", sanitize_name(category));
    let path = dir.join("spec").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_example(out: &mut String, fixture: &Fixture) {
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;

    writeln!(out, "  it {:?} do", fixture.id).unwrap();
    writeln!(out, "    # {}", fixture.description).unwrap();

    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();

    // Build route.
    writeln!(out, "    route = E2EHelpers::MockRoute.new(").unwrap();
    writeln!(out, "      path: {:?},", endpoint).unwrap();
    writeln!(out, "      method: {:?},", http_method).unwrap();
    writeln!(out, "      status: {status},").unwrap();
    writeln!(out, "      body: {},", ruby_string(&body_json)).unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "      stream_chunks: []").unwrap();
    } else {
        writeln!(out, "      stream_chunks: [").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "        {},", ruby_string(&chunk_json)).unwrap();
        }
        writeln!(out, "      ]").unwrap();
    }

    writeln!(out, "    )").unwrap();
    writeln!(out, "    server = E2EHelpers::MockServer.new([route])").unwrap();
    writeln!(out).unwrap();

    // Call the API.
    match method {
        "chat" => emit_ruby_chat_test(out, fixture, endpoint),
        "chat_stream" => emit_ruby_stream_test(out, fixture, endpoint),
        "embed" => emit_ruby_embed_test(out, fixture, endpoint),
        "list_models" => emit_ruby_list_models_test(out, fixture, endpoint),
        other => {
            writeln!(out, "    # TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out).unwrap();
    writeln!(out, "  ensure").unwrap();
    writeln!(out, "    server&.stop").unwrap();
    writeln!(out, "  end").unwrap();
    writeln!(out).unwrap();
}

