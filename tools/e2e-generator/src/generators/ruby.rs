fn write_cache_specs(dir: &Utf8Path, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "# Code generated by liter-llm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out, "# frozen_string_literal: true").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "require \"spec_helper\"").unwrap();
    writeln!(out, "require \"liter_llm\"").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "RSpec.describe \"cache\" do").unwrap();

    for fixture in fixtures {
        let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();
        let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
        let status = fixture.api.mock_response.status;
        let endpoint = endpoint_for_method(fixture.api.method.as_str());
        let is_stream = fixture.api.method == "chat_stream";

        writeln!(out, "  it {:?} do", fixture.id).unwrap();
        writeln!(out, "    # {}", fixture.description).unwrap();

        if is_stream && !fixture.api.mock_response.stream_chunks.is_empty() {
            writeln!(out, "    route = E2EHelpers::MockRoute.new(").unwrap();
            writeln!(out, "      path: {:?},", endpoint).unwrap();
            writeln!(out, "      method: \"POST\",").unwrap();
            writeln!(out, "      status: {status},").unwrap();
            writeln!(out, "      body: '',").unwrap();
            writeln!(out, "      stream_chunks: [").unwrap();
            for chunk in &fixture.api.mock_response.stream_chunks {
                let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
                writeln!(out, "        {},", ruby_string(&chunk_json)).unwrap();
            }
            writeln!(out, "      ]").unwrap();
            writeln!(out, "    )").unwrap();
        } else {
            writeln!(out, "    route = E2EHelpers::MockRoute.new(").unwrap();
            writeln!(out, "      path: {:?},", endpoint).unwrap();
            writeln!(out, "      method: \"POST\",").unwrap();
            writeln!(out, "      status: {status},").unwrap();
            writeln!(out, "      body: {},", ruby_string(&body_json)).unwrap();
            writeln!(out, "      stream_chunks: []").unwrap();
            writeln!(out, "    )").unwrap();
        }

        writeln!(out, "    server = E2EHelpers::MockServer.new([route])").unwrap();
        writeln!(out).unwrap();
        writeln!(
            out,
            "    client = LiterLlm::Client.new(api_key: 'test-key', base_url: server.url, max_retries: 0, cache: {{ max_entries: 10, ttl_seconds: 60 }})"
        )
        .unwrap();

        if is_stream {
            writeln!(out, "    chunks = client.chat_stream({})", ruby_string(&req_json)).unwrap();
            if fixture.assertions.cache_bypassed == Some(true) {
                writeln!(out, "    # Streaming requests should bypass cache entirely").unwrap();
                writeln!(out, "    expect(chunks).not_to be_empty").unwrap();
            }
        } else {
            writeln!(out, "    response1 = client.chat({})", ruby_string(&req_json)).unwrap();
            writeln!(out, "    response2 = client.chat({})", ruby_string(&req_json)).unwrap();

            if fixture.assertions.cache_hit == Some(true) {
                writeln!(out, "    # Second identical request should be a cache hit").unwrap();
                writeln!(out, "    body1 = JSON.parse(response1)").unwrap();
                writeln!(out, "    body2 = JSON.parse(response2)").unwrap();
                writeln!(
                    out,
                    "    expect(body2.dig('choices', 0, 'message', 'content')).to eq(body1.dig('choices', 0, 'message', 'content'))"
                )
                .unwrap();
            } else if fixture.assertions.cache_hit == Some(false) {
                writeln!(out, "    # Cache miss expected (e.g. TTL expired)").unwrap();
                writeln!(out, "    expect(response1).not_to be_nil").unwrap();
                writeln!(out, "    expect(response2).not_to be_nil").unwrap();
            }
        }

        writeln!(out).unwrap();
        writeln!(out, "  ensure").unwrap();
        writeln!(out, "    server&.stop").unwrap();
        writeln!(out, "  end").unwrap();
        writeln!(out).unwrap();
    }

    writeln!(out, "end").unwrap();

    let out = out.trim_end().to_string() + "\n";
    let path = dir.join("spec").join("cache_spec.rb");
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write cache_spec.rb: {e}"))
}

