fn write_cache_tests(dir: &Utf8Path, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "# Code generated by liter-llm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out, "defmodule LiterLlmE2E.CacheTest do").unwrap();
    writeln!(out, "  use ExUnit.Case, async: true").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "  alias LiterLlmE2E.MockServer").unwrap();
    writeln!(out, "  alias LiterLlmE2E.Helpers").unwrap();
    writeln!(out).unwrap();

    for fixture in fixtures {
        let test_name = fixture.description.replace('"', "'");
        let body_json = serde_json::to_string(&fixture.api.mock_response().body).unwrap_or_default();
        let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
        let status = fixture.api.mock_response().status;
        let endpoint = endpoint_for_method(fixture.api.method.as_str());
        let http_method = if fixture.api.method == "list_models" {
            "GET"
        } else {
            "POST"
        };
        let is_stream = fixture.api.method == "chat_stream";

        writeln!(out).unwrap();

        writeln!(out, "  test {test_name:?} do").unwrap();

        // Build routes
        writeln!(out, "    routes = [").unwrap();
        writeln!(out, "      %{{").unwrap();
        writeln!(out, "        path: {:?},", endpoint).unwrap();
        writeln!(out, "        method: {:?},", http_method).unwrap();
        writeln!(out, "        status: {status},").unwrap();
        writeln!(out, "        body: {:?},", body_json).unwrap();

        if is_stream && !fixture.api.mock_response().stream_chunks.is_empty() {
            writeln!(out, "        stream_chunks: [").unwrap();
            for chunk in &fixture.api.mock_response().stream_chunks {
                let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
                writeln!(out, "          {:?},", chunk_json).unwrap();
            }
            writeln!(out, "        ]").unwrap();
        } else {
            writeln!(out, "        stream_chunks: []").unwrap();
        }

        writeln!(out, "      }}").unwrap();
        writeln!(out, "    ]").unwrap();
        writeln!(out).unwrap();
        writeln!(out, "    {{:ok, base_url}} = MockServer.start(routes)").unwrap();
        writeln!(out).unwrap();

        writeln!(
            out,
            "    client = LiterLlm.client(api_key: \"test-key\", base_url: base_url, max_retries: 0, cache: [max_entries: 10, ttl_seconds: 60])"
        )
        .unwrap();

        if is_stream {
            writeln!(
                out,
                "    {{:ok, resp}} = Req.post(base_url <> {:?}, body: {:?}, headers: [{{\"content-type\", \"application/json\"}}], decode_body: false)",
                endpoint, req_json
            ).unwrap();
            writeln!(out, "    assert resp.status == 200").unwrap();
            writeln!(out, "    chunks = Helpers.collect_sse_chunks(resp.body)").unwrap();
            if fixture.assertions.cache_bypassed == Some(true) {
                writeln!(out, "    # Streaming requests should bypass cache entirely").unwrap();
                writeln!(out, "    assert length(chunks) >= 1, \"Expected stream chunks\"").unwrap();
            }
        } else {
            writeln!(out, "    {{:ok, resp1}} = LiterLlm.chat(client, {:?})", req_json).unwrap();
            writeln!(out, "    {{:ok, resp2}} = LiterLlm.chat(client, {:?})", req_json).unwrap();

            if fixture.assertions.cache_hit == Some(true) {
                writeln!(out, "    # Second identical request should be a cache hit").unwrap();
                writeln!(out, "    doc1 = Jason.decode!(resp1)").unwrap();
                writeln!(out, "    doc2 = Jason.decode!(resp2)").unwrap();
                writeln!(
                    out,
                    "    assert get_in(doc1, [\"choices\", Access.at(0), \"message\", \"content\"]) == get_in(doc2, [\"choices\", Access.at(0), \"message\", \"content\"])"
                )
                .unwrap();
            } else if fixture.assertions.cache_hit == Some(false) {
                writeln!(out, "    # Cache miss expected (e.g. TTL expired)").unwrap();
                writeln!(out, "    assert resp1 != nil").unwrap();
                writeln!(out, "    assert resp2 != nil").unwrap();
            }
        }

        writeln!(out, "  end").unwrap();
    }

    writeln!(out, "end").unwrap();

    let out = out.trim_end().to_string() + "\n";
    let path = dir.join("test").join("cache_test.exs");
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write cache_test.exs: {e}"))
}

