use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct ElixirGenerator;

impl Generator for ElixirGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let ex_dir = output_root.join("elixir");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&ex_dir);

        std::fs::create_dir_all(ex_dir.join("test").join("support"))
            .map_err(|e| anyhow::anyhow!("Failed to create elixir/test/support dir: {e}"))?;
        std::fs::create_dir_all(ex_dir.join("lib"))
            .map_err(|e| anyhow::anyhow!("Failed to create elixir/lib dir: {e}"))?;

        write_mix_exs(&ex_dir)?;
        write_formatter_exs(&ex_dir)?;
        write_test_helper(&ex_dir)?;
        write_mock_server(&ex_dir)?;
        write_helpers(&ex_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&ex_dir, category, cat_fixtures)?;
        }

        // Run mix format if available (best-effort; ignore errors).
        let _ = std::process::Command::new("mix")
            .arg("format")
            .current_dir(&ex_dir)
            .status();

        println!("Generated Elixir e2e tests in {ex_dir}");
        Ok(())
    }
}

